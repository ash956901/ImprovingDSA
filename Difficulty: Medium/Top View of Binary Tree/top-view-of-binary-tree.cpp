class Solution {
  public:
    // Function to return a list of nodes visible from the top view
    // from left to right in Binary Tree.
    vector<int> topView(Node *root) {
        vector<int> ans;
        queue<pair<Node*,pair<int,int>>> q;
        unordered_map<int,int> m;
        q.push(make_pair(root,make_pair(0,0)));
        int maxi=0;
        while(!q.empty()){
            auto front=q.front();
            q.pop();
            
            Node* node=front.first;
            int hd=front.second.first;
            int lvl=front.second.second;
            
            
            if(m.count(hd)==0){
                m[hd]=node->data;
            }
            
            maxi=max(maxi,abs(hd));
  
            
            if(node->left){
                q.push(make_pair(node->left,make_pair(hd-1,lvl+1)));
            }
            if(node->right){
                  q.push(make_pair(node->right,make_pair(hd+1,lvl+1)));
            }
        }
        
        
        int temp=-maxi;
        
        for(;temp<=maxi;temp++){
            if(m.count(temp)!=0) ans.push_back(m[temp]);
        }
        
       
       
        
        
        return ans;
    }
};



